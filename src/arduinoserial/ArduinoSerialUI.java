                lError.setForeground(Color.red);
                
                               
                window.mComPort = new ComPort("COM4");
                try {
                    window.mComPort.connect();
                } catch (NoSuchPortException ex) {
                    lError.setText(window.mComPort.portName + " not found!");
                } catch (PortInUseException ex) {
                    lError.setText(window.mComPort.portName + " in use!");
                } catch (UnsupportedCommOperationException | IOException ex) {
                    lError.setText("Something happend :)");
                }
                
                window.setVisible(true);
               
                window.mComPort.read(window);
                
